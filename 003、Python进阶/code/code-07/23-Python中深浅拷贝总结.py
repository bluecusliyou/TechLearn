'''
概念：深拷贝与浅拷贝
浅拷贝：只能拷贝外层对象，内层对象指向相同的内存空间
深拷贝：不仅可以拷贝外层对象，还可以拷贝内层对象，两者指向不同的内存空间
如果是不可变数据类型，无论是深拷贝还是浅拷贝，都无法实现复制，只能让其指向相同的内存空间

特殊情况：如果元组里面嵌套了一个列表，深拷贝有所不同，无论是外层还是内层对象都会被拷贝，两者指向了不同的内存空间
'''
import copy

tuple1 = (1, 2, 3, [4, 5])
tuple2 = copy.deepcopy(tuple1)

print(id(tuple1))
print(id(tuple2))

print(id(tuple1[3]))
print(id(tuple2[3]))